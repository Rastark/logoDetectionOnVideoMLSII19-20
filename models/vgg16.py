def vgg16_tiles(tile_dim, progress=True, **kwargs):
    r""" Output of the Conditioning Branch
    Tiles 1*1*512 vectors into tile_dim*tile_dim*512 tensors
    """
    model = _vgg('vgg16', 'X', False, progress, **kwargs)
